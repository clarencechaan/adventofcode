input = input.split("\n\n").map((str) => str.split("\n"));

function isLock(matrix) {
  return matrix[0] === "#####";
}

function convertToPinHeights(matrix) {
  const pinHeights = Array(5).fill(-1);
  for (let y = 0; y < 7; y++)
    for (let x = 0; x < 5; x++) if (matrix[y][x] === "#") pinHeights[x]++;
  return pinHeights;
}

function fits(lockHeights, keyHeights) {
  for (let i = 0; i < 5; i++)
    if (lockHeights[i] + keyHeights[i] > 5) return false;
  return true;
}

const lockHeightsArr = input
  .filter((matrix) => isLock(matrix))
  .map(convertToPinHeights);
const keyHeightsArr = input
  .filter((matrix) => !isLock(matrix))
  .map(convertToPinHeights);

let result = 0;
for (const lockHeights of lockHeightsArr)
  for (const keyHeights of keyHeightsArr)
    if (fits(lockHeights, keyHeights)) result++;
console.log(result);
