const n = input.length;
const m = input[0].length;

const regionsSeen = Array(n)
  .fill()
  .map(() => Array(m).fill(false));

function getRegion(char, x, y, region = []) {
  if (input[y]?.[x] !== char || regionsSeen[y][x]) return;
  regionsSeen[y][x] = true;
  region.push([x, y]);
  getRegion(char, x - 1, y, region);
  getRegion(char, x + 1, y, region);
  getRegion(char, x, y - 1, region);
  getRegion(char, x, y + 1, region);
  return region;
}

function getArea(region) {
  return region.length;
}

function getNumSides(region) {
  const char = input[region[0][1]][region[0][0]];

  const upMatrix = Array(n)
    .fill()
    .map(() => Array(m).fill(false));
  const downMatrix = Array(n)
    .fill()
    .map(() => Array(m).fill(false));
  const leftMatrix = Array(n)
    .fill()
    .map(() => Array(m).fill(false));
  const rightMatrix = Array(n)
    .fill()
    .map(() => Array(m).fill(false));

  for (const [x, y] of region) {
    if (input[y - 1]?.[x] !== char) upMatrix[y][x] = true;
    if (input[y + 1]?.[x] !== char) downMatrix[y][x] = true;
    if (input[y][x - 1] !== char) leftMatrix[y][x] = true;
    if (input[y][x + 1] !== char) rightMatrix[y][x] = true;
  }

  let numSides = 0;
  for (let y = 0; y < n; y++)
    for (let x = 0; x < m; x++) {
      if (upMatrix[y][x] && !upMatrix[y][x - 1]) numSides++;
      if (downMatrix[y][x] && !downMatrix[y][x - 1]) numSides++;
    }

  for (let x = 0; x < m; x++)
    for (let y = 0; y < n; y++) {
      if (leftMatrix[y][x] && !leftMatrix[y - 1]?.[x]) numSides++;
      if (rightMatrix[y][x] && !rightMatrix[y - 1]?.[x]) numSides++;
    }

  return numSides;
}

