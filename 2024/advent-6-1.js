input = input.split("\n").map((str) => str.split(""));

const n = input.length;
const m = input[0].length;

let x;
let y;

for (let i = 0; i < n; i++)
  for (let j = 0; j < m; j++)
    if (input[i][j] === "^") {
      x = j;
      y = i;
      break;
    }

// 0: up
// 1: right
// 2: down
// 3: left
let direction = 0;

function getNextSquareCoords(x, y, direction) {
  switch (direction) {
    case 0:
      return [x, y - 1];
    case 1:
      return [x + 1, y];
    case 2:
      return [x, y + 1];
    case 3:
      return [x - 1, y];
  }
}

function getNextSquare(x, y, direction) {
  const [nextX, nextY] = getNextSquareCoords(x, y, direction);
  return input[nextY]?.[nextX];
}

function move() {
  if (input[y]?.[x] === undefined) return false;

  input[y][x] = "X";
  while (getNextSquare(x, y, direction) === "#")
    direction = (direction + 1) % 4;

  [x, y] = getNextSquareCoords(x, y, direction);

  return true;
}

while (move());

const result = input.reduce(
  (sum, arr) => arr.reduce((sum, cell) => +(cell === "X") + sum, 0) + sum,
  0
);

