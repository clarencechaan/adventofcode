function addHorizontal() {
  for (let i = 0; i < n; i++)
    for (let j = 0; j < m; j++) input[i].push(input[i][j]);
}

while (n * 7 + 1 > input[0].length) addHorizontal();

let output = [0, 0, 0, 0, 0];
for (let x = 0, y = 0; y < n; y++, x++) if (input[y][x] === "#") output[0]++;
for (let x = 0, y = 0; y < n; y++, x += 3) if (input[y][x] === "#") output[1]++;
for (let x = 0, y = 0; y < n; y++, x += 5) if (input[y][x] === "#") output[2]++;
for (let x = 0, y = 0; y < n; y++, x += 7) if (input[y][x] === "#") output[3]++;
for (let x = 0, y = 0; y < n; y += 2, x++) if (input[y][x] === "#") output[4]++;

output = output.reduce((prod, val) => prod * val, 1);
