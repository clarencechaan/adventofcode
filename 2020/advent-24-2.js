input = input.split("\n");
let lines = [];
for (let i = 0; i < input.length; i++) {
  lines[i] = [];
  for (let j = 0; j < input[i].length; j++) {
    if (input[i][j] === "w" || input[i][j] === "e") lines[i].push(input[i][j]);
    else {
      lines[i].push(input[i].slice(j, j + 2));
      j++;
    }
  }
}

const n = 150;
let grid = [];
for (let i = 0; i < n; i++) grid.push(Array(n).fill(0));

for (const line of lines) {
  let x = n / 2;
  let y = n / 2;
  for (const step of line) {
    switch (step) {
      case "e":
        x++;
        break;
      case "se":
        if (y % 2 === 0) x++;
        y++;
        break;
      case "sw":
        if (y % 2 !== 0) x--;
        y++;
        break;
      case "w":
        x--;
        break;
      case "nw":
        if (y % 2 !== 0) x--;
        y--;
        break;
      case "ne":
        if (y % 2 === 0) x++;
        y--;
        break;
    }
  }
  grid[y][x] = (grid[y][x] + 1) % 2;
}

function getNumNeighbours(x, y, grid) {
  if (y % 2 === 0)
    return (
      grid[y][x - 1] +
      grid[y][x + 1] +
      grid[y - 1]?.[x] +
      grid[y - 1]?.[x + 1] +
      grid[y + 1]?.[x] +
      grid[y + 1]?.[x + 1]
    );
  else
    return (
      grid[y][x - 1] +
      grid[y][x + 1] +
      grid[y - 1]?.[x - 1] +
      grid[y - 1]?.[x] +
      grid[y + 1]?.[x - 1] +
      grid[y + 1]?.[x]
    );
}

for (let day = 0; day < 100; day++) {
  let nextGrid = [];
  for (const row of grid) nextGrid.push([...row]);
  for (let i = 0; i < n; i++)
    for (let j = 0; j < n; j++) {
      let numNeighbours = getNumNeighbours(j, i, grid);
      if (grid[i][j] === 1 && (numNeighbours === 0 || numNeighbours > 2))
        nextGrid[i][j] = 0;
      else if (grid[i][j] === 0 && numNeighbours === 2) nextGrid[i][j] = 1;
    }
  grid = nextGrid;
}

let output = grid.reduce(
  (sum, row) => sum + row.reduce((sum, tile) => sum + tile, 0),
  0
);
