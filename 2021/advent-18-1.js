function markDepths(tree, depth = 0) {
  if (!tree) return;
  tree.depth = depth;
  markDepths(tree.left, depth + 1);
  markDepths(tree.right, depth + 1);
}

function markParents(tree, parent) {
  if (!tree) return;
  if (parent) tree.parent = parent;
  markParents(tree.left, tree);
  markParents(tree.right, tree);
}

function getLeaves(tree, leaves = []) {
  if (!tree.left && !tree.right) {
    leaves.push(tree);
    return;
  }
  getLeaves(tree.left, leaves);
  getLeaves(tree.right, leaves);
  return leaves;
}

function explodeTree(idx, leaves) {
  if (idx - 1 >= 0) leaves[idx - 1].val += leaves[idx].val;
  if (idx + 2 <= leaves.length - 1) leaves[idx + 2].val += leaves[idx + 1].val;
  let parent = leaves[idx].parent;
  parent.val = 0;
  parent.left = null;
  parent.right = null;
  leaves.splice(idx, 2, parent);
}

function splitTree(idx, leaves) {
  let left = new TreeNode(Math.floor(leaves[idx].val / 2));
  let right = new TreeNode(Math.ceil(leaves[idx].val / 2));
  left.depth = leaves[idx].depth + 1;
  right.depth = leaves[idx].depth + 1;
  left.parent = leaves[idx];
  right.parent = leaves[idx];
  leaves[idx].left = left;
  leaves[idx].right = right;
  delete leaves[idx].val;
  leaves.splice(idx, 1, left, right);
}

