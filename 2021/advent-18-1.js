function TreeNode(val = null, left = null, right = null) {
  if (val !== null) this.val = val;
  this.left = typeof left === "string" ? new TreeNode(parseInt(left)) : left;
  this.right =
    typeof right === "string" ? new TreeNode(parseInt(right)) : right;
}

function convertStrToTreeNode(str) {
  let arr = str.split("");
  while (arr.length > 1)
    for (let i = 4; i < arr.length; i++)
      if (arr[i - 4] === "[" && arr[i] === "]")
        arr = [
          ...arr.slice(0, i - 4),
          new TreeNode(null, arr[i - 3], arr[i - 1]),
          ...arr.slice(i + 1),
        ];
  return arr[0];
}

