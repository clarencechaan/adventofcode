let output = [];

loop: for (const line of input) {
  let str = line;
  while (
    str.includes("()") ||
    str.includes("[]") ||
    str.includes("{}") ||
    str.includes("<>")
  ) {
    str = str.replaceAll("()", "");
    str = str.replaceAll("[]", "");
    str = str.replaceAll("{}", "");
    str = str.replaceAll("<>", "");
  }

  for (let i = 1; i < str.length; i++)
    if (
      ((str[i - 1] === "[" || str[i - 1] === "{" || str[i - 1] === "<") &&
        str[i] === ")") ||
      ((str[i - 1] === "(" || str[i - 1] === "{" || str[i - 1] === "<") &&
        str[i] === "]") ||
      ((str[i - 1] === "[" || str[i - 1] === "(" || str[i - 1] === "<") &&
        str[i] === "}") ||
      ((str[i - 1] === "[" || str[i - 1] === "{" || str[i - 1] === "(") &&
        str[i] === ">")
    )
      continue loop;

  const scoreDict = { "(": 1, "[": 2, "{": 3, "<": 4 };

  let score = 0;
  for (let i = str.length - 1; i >= 0; i--)
    score = score * 5 + scoreDict[str[i]];
  output.push(score);
}

output.sort((a, b) => (a > b ? 1 : -1));
