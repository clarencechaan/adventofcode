let grid = input.split("\n").map((str) => str.split(""));
const n = grid.length;
const m = grid[0].length;

function tilt(str, direction) {
  let grid = str.split("\n").map((str) => str.split(""));

  switch (direction) {
    case 0:
      // north
      for (let col = 0; col < m; col++)
        for (let row = 1; row < n; row++) {
          while (grid[row]?.[col] === "O" && grid[row - 1]?.[col] === ".") {
            [grid[row][col], grid[row - 1][col]] = [
              grid[row - 1][col],
              grid[row][col],
            ];
            row--;
          }
        }
      break;
    case 1:
      // west
      for (let row = 0; row < n; row++)
        for (let col = 1; col < m; col++) {
          while (grid[row]?.[col] === "O" && grid[row][col - 1] === ".") {
            [grid[row][col], grid[row][col - 1]] = [
              grid[row][col - 1],
              grid[row][col],
            ];
            col--;
          }
        }
      break;
    case 2:
      // south
      for (let col = 0; col < m; col++)
        for (let row = n - 2; row >= 0; row--) {
          while (grid[row]?.[col] === "O" && grid[row + 1]?.[col] === ".") {
            [grid[row][col], grid[row + 1][col]] = [
              grid[row + 1][col],
              grid[row][col],
            ];
            row++;
          }
        }
      break;
    case 3:
      // east
      for (let row = 0; row < n; row++)
        for (let col = m - 2; col >= 0; col--) {
          while (grid[row]?.[col] === "O" && grid[row][col + 1] === ".") {
            [grid[row][col], grid[row][col + 1]] = [
              grid[row][col + 1],
              grid[row][col],
            ];
            col++;
          }
        }
      break;
  }

  return grid.map((arr) => arr.join("")).join("\n");
}

let str = input;
let map = {};

for (let cycles = 0; cycles < 1000; cycles++) {
  if (map[str]) {
    map[str][1].push(cycles);
    str = map[str][0];
  } else {
    let result = str;
    for (let direction = 0; direction < 4; direction++)
      result = tilt(result, direction);
    map[str] = [result, [cycles]];
    str = result;
  }
}

for (let str in map) {
  if (map[str][1].length < 3) continue;
  let last2 = map[str][1].slice(-2);
  let diff = last2[1] - last2[0];
  if ((1000000000 - last2[1]) % diff === 0) {
    grid = str.split("\n").map((str) => str.split(""));
    break;
  }
}

let output = 0;
for (let i = 0; i < n; i++)
  for (let j = 0; j < m; j++) if (grid[i][j] === "O") output += n - i;
