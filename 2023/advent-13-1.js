function doesHorMirrorExist(grid, mirror) {
  for (let i = 0; mirror + i < grid.length && mirror - i - 1 >= 0; i++)
    for (let j = 0; j < grid[0].length; j++)
      if (grid[mirror + i][j] !== grid[mirror - i - 1][j]) return false;
  return true;
}

function doesVertMirrorExist(grid, mirror) {
  for (let j = 0; mirror + j < grid[0].length && mirror - j - 1 >= 0; j++)
    for (let i = 0; i < grid.length; i++)
      if (grid[i][mirror + j] !== grid[i][mirror - j - 1]) return false;
  return true;
}

function getHorMirror(grid) {
  for (let mirror = 1; mirror < grid.length; mirror++)
    if (doesHorMirrorExist(grid, mirror)) return mirror;
  return -1;
}

function getVertMirror(grid) {
  for (let mirror = 1; mirror < grid[0].length; mirror++)
    if (doesVertMirrorExist(grid, mirror)) return mirror;
  return -1;
}

function getSummary(grid) {
  let mirror = getHorMirror(grid);
  if (mirror >= 1) return 100 * mirror;
  mirror = getVertMirror(grid);
  return mirror;
}

let output = 0;
for (const grid of input) output += getSummary(grid);
